document.write("While loop", "<br><br>");
var i1 = 0;
while (i1 <= 3) {
    document.write(i1, "<br>");
    i1++;
}

document.write("Do while loop", "<br><br>");
var i2 = 0;
do {
    document.write(i2, "<br>");
    i2++;
} while (i2 <= 6);

